# reverse the list in-place, so the oldest Rides are at the beginning
ACTIVITY_IDS.reverse()

class Command(BaseCommand):
    help = "Loads all Strava activities into the database if they don't already exist."

    def add_arguments(self, parser):
        parser.add_argument(
            "--force-update",
            action="store_true",
            help="Update all activities even if they already exist. Useful when adding new fields to the Activity Model.",
        )

    def handle(self, *args, **options):
        rf = RequestFactory()
        force_update = options["force_update"]
        total = len(ACTIVITY_IDS)
        loaded = 0
        skipped = 0

        for idx, activity_id in enumerate(ACTIVITY_IDS, start=1):
            # Skip if the activity already exists in DB
            if not force_update and Activity.objects.filter(strava_id=activity_id).exists():
                self.stdout.write(
                    self.style.WARNING(f"[{idx}/{total}] Skipping activity {activity_id} (already exists)")
                )
                skipped += 1
                continue
            elif force_update:
                # sleep a bit to avoid hitting rate limits
                import time
                print("Sleeping for 10 seconds to avoid rate limits...")
                time.sleep(10)

            # Prepare a fake request to reuse the existing view
            request = rf.get(f"/strava/load/{activity_id}/")

            try:
                response = load_activity(request, activity_id)
                if response.status_code == 200:
                    self.stdout.write(self.style.SUCCESS(f"[{idx}/{total}] ✓ Loaded activity {activity_id}"))
                    loaded += 1
                else:
                    self.stdout.write(self.style.ERROR(
                        f"[{idx}/{total}] ✗ Failed to load {activity_id}: HTTP {response.status_code}"))
            except Exception as e:
                self.stdout.write(self.style.ERROR(f"[{idx}/{total}] ✗ Error loading {activity_id}: {e}"))

        self.stdout.write("")
        self.stdout.write(self.style.SUCCESS(f"✅ Done! {loaded} loaded, {skipped} skipped, total {total}"))
